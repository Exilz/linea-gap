<h1>Accesibilité</h1>
<p>L’accessibilité  aux  transports  est  une  préoccupation  constante  de  la  Ville  de  Gap  et  la  politique 
volontariste  menée  depuis  de  nombreuses  années  doit  permettre  d’offrir  prochainement  un  service 
public de transport accessible à tous.</p>

<p>Le Schéma Directeur d’Accessibilité du  réseau de transports en  commun  voté en Conseil municipal 
le 9 décembre 2010 a permis de définir et mettre en œuvre les obligations de la loi du 11 février 2005 
pour l'égalité des droits et des chances, la participation et la citoyenneté des personnes handicapées.
Le  parc  roulant  du  réseau  Linéa  avec  ses  9  lignes  de  bus  et  ses  3  navettes  de  centre-ville  est 
aujourd’hui accessible à plus de 90%.</p>

<p>Les  bus  du  réseau  sont  à  plancher  bas  et  disposent  de  palettes  électriques  ou  manuelles.  Ils  sont 
en outre équipés de un  voire deux emplacements pour personnes en fauteuil  roulant et d’annonces 
visuelles et sonores des arrêts pour 60% d’entre-eux.</p>

<p>L’information aux arrêts est conforme à la réglementation et les numéros de lignes et les destinations 
sont affichés sur les faces avant, arrière et latérale des véhicules.
Enfin,  prés  de  60%  des  arrêts  sont  aujourd’hui  accessibles  sur  le  réseau  urbain  aux  personnes 
en  situation  de  handicap  et  ont  fait  l’objet  d’un  rehaussement  de  quai  et  d’un  traitement  des 
cheminements aux abords.</p>

<p>Chaque année 50 000 € sont consacrées à la mise en accessibilité des arrêts de bus. 
Ces  travaux  et  aménagements  sont  menés  en  concertation  avec  le  monde  associatif  chargé  du 
handicap au sein de la Commission municipale d’accessibilité.</p>

