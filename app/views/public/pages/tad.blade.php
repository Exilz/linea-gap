<h1>Services de transports à la demande (TAD)</h1>
<p>Les  services  de  Transport  à  la  Demande  (TAD)  fonctionnent  toute  l’année  du  lundi  au  samedi,  ils
permettent de se déplacer depuis et vers les secteurs non desservis par les lignes régulières.</p>

<p>Pour  utiliser  ces  services  il  suffit  de  réserver  la  veille  jusqu’à  17h30  pour  les  transports  dans  la
matinée,  le matin  jusqu'à  11h30  pour  les transports  dans  l'après-midi.  Les  lignes  et  les  horaires  de 
passage sont prédéfinis.</p>

<p>La  réservation  s’effectue  en  enregistrant  les Nom  et Prénom  du  demandeur,  l'horaire  souhaité  ainsi 
que les arrêts de départ et de destination.</p>

<p>Un véhicule se présentera à l'arrêt à l'horaire convenu. Le tarif de 1€50 par personne et par trajet est à 
acquitter auprès du conducteur.</p>

