<h1>Lignes et navettes relais</h1>

<h2>Linéa Centro A</h2>
<p>La  navette  du  cœur  de  ville  fonctionne  du  mardi  au  samedi.  Reliant  la  gare  SNCF  au  parking  de 
la  Commanderie  à travers  le  centre  ville,  c’est  une  autre façon  de  circuler  rapidement  dans  l’hyper 
centre.</p>

<h2>Linéa Centro B</h2>
<p>Cette navette dessert les quartiers de Crève-Cœur, de Saint Mens, de Kapados et la zone d’activité 
de Tokoro. Les habitants proches du centre ville peuvent ainsi se déplacer en toute liberté.</p>

<h2>Ligne 20 / NRE Navette Relais Express</h2>
<p>Du lundi au vendredi toute l’année, cette navette dessert le parking relais de Malcombe. Les usagers 
peuvent  laisser  leur  voiture  sur  ce  parking  gratuit  et  accéder  au  centre  ville  en  10  minutes  sans  la 
contrainte de la circulation et du stationnement. Cette ligne est accessible aux vélos.</p>

<h2>Ligne 73 / Charance – Centre Ville – Bayard</h2>
<p>Tous les jours du 1er juillet au 31 août, cette navette permet de découvrir deux sites exceptionnels.</p>

<p>Au  Domaine  de  Charance,  le  Service Municipal  d’Animation  propose  des  visites  accompagnées  de
la roseraie et du jardin remarquable. Des ateliers et des expositions sont organisés dans les écuries 
du Château qui abrite le siège du Parc National des Ecrins. Les promeneurs pourront à l’envie, flâner 
autour du lac ou partir sur les sentiers de randonnée à l’assaut des crêtes.</p>

<p>Le Centre d’Oxygénation de Gap Bayard se situe dans un panorama unique. Autour du golf 18 trous, 
le  plateau  de  Bayard  offre  de  nombreux  parcours  de  promenade  et  de  VTT.  A  1250  m  d’altitude 
entre Alpes et Provence, la vue s’ouvre successivement vers les massifs du gapençais et du Val de 
Durance, vers les contreforts du Champsaur et du Dévoluy, et vers les grands sommets des Ecrins.</p>

<p>Le  véhicule  de  grande  capacité  permet  d’accueillir  les  groupes,  et  est  équipé  pour  transporter  les
vélos.</p>

<h2>Ligne 75 / Gap – Espace Nordique de Bayard – Station de Laye</h2>
<p>Cette ligne fonctionne tous les jours pendant les  vacances de Noël et de février. Chacun peut ainsi
goûter aux plaisirs de la neige. Le véhicule de 50 places peut transporter des groupes, il est équipé 
d’une soute pour transporter le matériel de ski.</p>

<p>L’Espace  Nordique  de  Bayard  propose  ses  50  kms  de  pistes  de  ski  de  fond  (traçage  classique  ou
skating), des parcours de promenade pour piétons et raquettes sur des itinéraires vallonnés et balisés, 
et  une  piste  de  luge  pour  les  enfants.  Le  site Gap-Bayard  a  obtenu  le  label  4  nordiques  délivré  par 
Nordic France.</p>

<p>Pour  une  journée  sportive  ou  de  détente,  le  restaurant  et  le  bar  du  Centre  d'Oxygénation  vous
accueillent tous les jours en groupe ou en famille.</p>

<p>La  station  village  de  Laye  blottie  au  pied  du  Pic  de  Gleize  et  de  l'Aiguille,  entre  1300  et  1950  m
d'altitude,  est  un  lieu  idéal  pour  toute  la  famille.  Sur  place,  école  de  ski,  jardin  d'enfants,  terrasses 
ensoleillées et convivialité offrent les avantages d'une station moderne.</p>

<p>Les 10 km de pistes desservies par 5 téléskis et 2 tapis skieurs proposent un front de neige adapté à 
la découverte de la glisse, et des pistes plus sportives sur le haut de la station. L'espace ludique "Folie 
de  la  Glisse"  procure  des  sensations  et  permet  à  chacun  de trouver  sa  glisse :  snowpark,  piste  de 
slalom  chronométré, tremplin  de  saut  «  big  airbag  »,  piste  de  luge  et  boarder  cross.  5  parcours  en 
raquettes permettent de découvrir de jolis points de vue sur la vallée du Champsaur.</p>

